public class Core_Java_Operations_Example {

	public static void main(String[] args) {

		String str2 = "Sumit";
		String str1 = "Badgujar";
		String Str3 = "Sanjay";
		
		
		//Concatenation Operation
		System.out.println("Concatenated String: "+str1+" "+str2+" "+Str3);
		
		
		//Repeat Printing Operation
		System.out.println("Repeated String: "+str1.repeat(3));
		
		
		//Length Operation 
		System.out.println("Length of String: "+str1.length());
		
		
		//Minimum Operation
		System.out.println();
		

	}

}
